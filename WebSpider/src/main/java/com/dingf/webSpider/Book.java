package com.dingf.webSpider;

public class Book {
	public String name;//书名
	public String aveComment;//评分
	public String commentNum;//评价人数
	public String author;//作者
	public String chuBanShe;//出版社
	public String time;//出版日期
	public String price;//价格
	
	public Book(){
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAveComment() {
		return aveComment;
	}
	public void setAveComment(String aveComment) {
		this.aveComment = aveComment;
	}
	public String getCommentNum() {
		return commentNum;
	}
	public void setCommentNum(String commentNum) {
		this.commentNum = commentNum;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getChuBanShe() {
		return chuBanShe;
	}
	public void setChuBanShe(String chuBanShe) {
		this.chuBanShe = chuBanShe;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
